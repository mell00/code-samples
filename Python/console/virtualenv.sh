#PYTHON virtualenv#

$pip3 install virtualenv

$pip3 list

$mkdir directory_for_virtualenvs

$cd directory_for_virtualenvs

$ls directory_for_virtualenvs

$virtualenv project_virtenv_name

$source project_virtenv_name/bin/activate

$which python

$deactivate

$rm -rf project_virtenv_name

$virtualenv -p /usr/bin/python_version project_virtenv_name

$python --version
